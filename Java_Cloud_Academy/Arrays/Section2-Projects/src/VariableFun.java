public class VariableFun {
    public static void main(String[] args) {
       int age;  // Declaration
       age = 19;  // Initialization
       String name = "Billy";
       String city = "London";

       System.out.println(name + " is " + age + " years old.");
       System.out.println(name + " is living in " + city + ".");

    }
}
